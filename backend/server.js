import express from 'express';
import { createServer } from 'http';
import { Server as SocketIOServer } from 'socket.io';
import mongoose from 'mongoose';
import cors from 'cors';
import helmet from 'helmet';
import rateLimit from 'express-rate-limit';
import dotenv from 'dotenv';

import mentionRoutes from './src/routes/mentionRoutes.js';
import alertRoutes from './src/routes/alertRoutes.js';
import analyticsRoutes from './src/routes/analyticsRoutes.js';
import { startDataCollection } from './src/services/dataCollector.js';
import { createErrorHandler } from './src/utils/errors.js';

dotenv.config();

const app = express();
const server = createServer(app);
const io = new SocketIOServer(server, {
  cors: {
    origin: process.env.FRONTEND_URL || "http://localhost:3000",
    methods: ["GET", "POST"]
  }
});

// Middleware
app.use(helmet());
app.use(cors());
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

// Rate limiting
const limiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutes
  max: 100 // limit each IP to 100 requests per windowMs
});
app.use('/api/', limiter);

// MongoDB Connection
mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/brandtracker', {
  useNewUrlParser: true,
  useUnifiedTopology: true,
})
.then(() => console.log('âœ… Connected to MongoDB'))
.catch(err => console.error('âŒ MongoDB connection error:', err));

// Routes
app.use('/api/mentions', mentionRoutes);
app.use('/api/alerts', alertRoutes);
app.use('/api/analytics', analyticsRoutes);

// Health check endpoint
app.get('/api/health', (req, res) => {
  res.json({ 
    status: 'OK', 
    timestamp: new Date().toISOString(),
    service: 'Brand Mention Tracker API'
  });
});

// Socket.io connection handling
io.on('connection', (socket) => {
  console.log('ğŸ”Œ Client connected:', socket.id);
  
  socket.on('disconnect', () => {
    console.log('ğŸ”Œ Client disconnected:', socket.id);
  });
});

// Make io available globally for real-time updates
global.io = io;

// Start data collection services
startDataCollection();

// Error handling middleware
app.use(createErrorHandler());

// 404 handler
app.use('*', (req, res) => {
  res.status(404).json({ error: 'Route not found' });
});

const PORT = process.env.PORT || 5000;
server.listen(PORT, () => {
  console.log(`ğŸš€ Server running on port ${PORT}`);
  console.log(`ğŸŒ API available at http://localhost:${PORT}/api`);
});

export { app, server, io };
export default { app, server, io };